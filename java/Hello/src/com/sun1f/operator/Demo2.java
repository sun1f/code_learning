package com.sun1f.operator;

public class Demo2 {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        // '+'可以实现字符串的拼接
        System.out.println("a && b = " + (a && b));
        System.out.println("a || b = " + (a || b));
        System.out.println("!a = " + (!a));
        System.out.println("!b = " + (!b));
    }
}
